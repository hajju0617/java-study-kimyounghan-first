package loop;

public class For1 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println("i = " + i);
        }

        /*
        정해진 횟수만큼 반복을 수행해야 하면 for
        그렇지 않으면 while
        단 이것이 항상 정답은 아니고 기준으로 삼는 정도.

        만약 사용자가 1, 2, 3을 누르고 4를 누르면 종료 시켜라
        or 사용자가 종료 버튼을 누르면 종료
        -> 이땐 while
         */
    }
}
